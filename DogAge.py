# First Dog Age Calculator

#1. Input:
sAge = input("Input your dogs age: ")

#2. Convert:
fAge = float(sAge)

# 3. Compute
fHumanAge = fAge * 7.3

# 4. Output:

print("The human age equivalent for your dog is: " + str(fHumanAge))
print("The human age equivalent formatted for your dog is: " + format(fHumanAge,'.1f'))
      
